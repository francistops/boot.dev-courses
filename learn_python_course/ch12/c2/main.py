def make_purchases(purchase_orders):
    leftover = []
    for po in purchase_orders:
        try:
            money_leftover = purchase(po['price'],
                                      po['money_available'])
        except Exception as err:
            print(err)
        if money_leftover > 0:
            leftover.append(money_leftover)
    return leftover

